    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here://///   زر اضافة مستخدم
        if (!txtname.getText().isEmpty() && !txtpass.getText().isEmpty()) {
            String statement = "INSERT INTO users(name, password) VALUES('" + txtname.getText() + "', '" + txtpass.getText() + "' ) ;";
            boolean check = Tools.ExexcuteStatement(statement);
            if (check) {
                
                JOptionPane.showMessageDialog(null, "تمت الاضافه بنجاح");
                reset_Value();
                updateTable();
                
            }
            Tools.CloseConnection();
        } else {
            JOptionPane.showMessageDialog(null, "يجب ادخال اسم المستخدم وكلمه المرور");
        }
    }//GEN-LAST:event_btnaddActionPerformed

