    ///   هذه الدالة وظيفتها تأخذ المعلومات من جدول العيادات  من قاعدة البيانات وتعرضها في جدول العيادات 
    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        idusers.removeAllElements();
        String statement = "SELECT * FROM users";
        ResultSet rs = Tools.select_Query(statement);
        try {
            int i=1;
            while (rs.next()) {
                // هنا نأخذالايديهات ونخزنهافي كل دورة حيث سوف نحتاجها
                idusers.add(rs.getString(1));/// كان هنا ياخذ الايدي ويضهره في الجدول ولكن نريد ان يظهر  فقط ارقام ترقيم للصفوف اضفنا الاي 
                Vector v = new Vector();            //////////////       اما الايدي سوف نجعله مخفي نستخدمه فقط شرط للاضافة والتعديل
                v.add(i);
                v.add(rs.getString(2));//// هنا معا جدول العرض نبدأ من واحد وليس من صفر
                v.add(rs.getString(3));
                model.addRow(v);
                i++;
            }
        } catch (Exception ex) {
        }
        Tools.CloseConnection();
    }
